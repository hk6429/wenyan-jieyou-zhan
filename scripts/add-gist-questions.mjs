function balanceDistractors(options) {
  const next = options.slice();
  const lengths = next.map((s) => [...s].length);
  const correctLength = lengths[0];
  if (lengths.filter((n) => n === correctLength).length === 1 && correctLength - Math.min(...lengths) >= 4) {
    const target = lengths.slice(1).indexOf(Math.max(...lengths.slice(1))) + 1;
    next[target] += '，作為全文主要內容';
  }
  return next;
}

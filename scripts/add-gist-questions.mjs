const texts = JSON.parse(readFileSync(DATA_URL, 'utf8'));
let added = 0;
let updated = 0;
for (const [textId, rows] of Object.entries(additions)) {
  const text = texts.find((t) => t.id === textId);
  if (!text) throw new Error(`找不到 ${textId}`);
  for (const [id, segment, stem, correct, d1, d2, d3, explain, evidence] of rows) {
    const source = text.segments.find((s) => Number(s.no) === segment);
    const corpus = `${source?.text || ''}\n${source?.translation || ''}\n${source?.note || ''}`;
    if (!source || !corpus.includes(evidence)) throw new Error(`${id} grounding 無法回查：「${evidence}」`);
    const question = { id, type: 'gist', stem, options: balanceDistractors([correct, d1, d2, d3]), answer: 0, explain, grounding: { review, segment, evidence } };
    const existing = text.questions.findIndex((q) => q.id === id);
    if (existing >= 0) {
      if (text.questions[existing].grounding?.review !== review) throw new Error(`${id} 已存在且不是本輪題目`);
      text.questions[existing] = question;
      updated += 1;
    } else {
      text.questions.push(question);
      added += 1;
    }
  }
}

writeFileSync(DATA_URL, `${JSON.stringify(texts, null, 2)}\n`);
console.log(`grounded 段旨題：新增 ${added} 題、更新 ${updated} 題。`);
for (const text of texts) {
  const n = text.questions.filter((q) => q.type === 'gist').length;
  if (n < 5) throw new Error(`${text.id} 仍只有 ${n} 題段旨題`);
}
